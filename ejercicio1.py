#Crear una lista con los números del 1 al 100 que sean múltiplos de 4. Utilizar la función range.

#Le asignamos a la variable lista_numeros la lista en rango 0 al 101 (para que incluya el 100) en intervalos de 4 para mostrar sus múltiplos.
lista_numeros = list(range(0, 101, 4)) 
#Mostramos el resultado en pantalla.
print(lista_numeros)